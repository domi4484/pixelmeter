// Files includes --------------------------
#include "Body.h"

// Qt includes -----------------------------
#include <QMouseEvent>

//-----------------------------------------------------------------------------------------------------------------------------

Body::Body(QWidget *parent) :
  QWidget(parent),
  m_ClickPos()
{
}

//-----------------------------------------------------------------------------------------------------------------------------

Body::~Body()
{

}

//-----------------------------------------------------------------------------------------------------------------------------

void Body::mousePressEvent(QMouseEvent *event)
{
  m_ClickPos = event->pos();
}

//-----------------------------------------------------------------------------------------------------------------------------

void Body::mouseMoveEvent(QMouseEvent *event)
{
  emit signal_moveRequest(event->globalPos() - m_ClickPos);
}

